#include "../minishell.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*p;

	i = 0;
	if (!s)
		return (NULL);
	p = malloc(ft_strlen(s) + 1);
	if (p == NULL)
		return (NULL);
	while (s[i])
	{
		p[i] = s[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
